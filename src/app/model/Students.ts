export class Student {
    id: string;
    age: number;
    firstName: string;
    lastName: string;
    email: string;
    constructor(id: string, age: number, firstName: string, lastName: string, email: string) {
        this.id = id;
        this.age = age;
        this.firstName = firstName;
        this.lastName = lastName;
        this.email = email;
    }
}
    